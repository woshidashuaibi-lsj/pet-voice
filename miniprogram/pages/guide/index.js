Page({
  data: {
    petTabs: ['猫咪', '狗狗'],
    activePetTab: '猫咪',
    stageList: [],
    showDetail: false,
    currentItem: null,
    statusBarHeight: 0,
    navBarHeight: 44,
  },

  onLoad() {
    this.setData({
      statusBarHeight: app.globalData.statusBarHeight || 0,
      navBarHeight:    app.globalData.navBarHeight || 44,
    })

    // 优先根据已登记的宠物类型显示对应 tab
    const petInfo = wx.getStorageSync('petInfo')
    if (petInfo && petInfo.type === 'dog') {
      this.setData({ activePetTab: '狗狗' })
    }

    this._buildStageList()
  },

  switchPetTab(e) {
    this.setData({ activePetTab: e.currentTarget.dataset.tab })
    this._buildStageList()
  },

  _buildStageList() {
    const key = this.data.activePetTab === '猫咪' ? 'cat' : 'dog'
    this.setData({ stageList: GUIDE_DATA[key] || [] })
  },

  openDetail(e) {
    const { stageid, itemid } = e.currentTarget.dataset
    const key = this.data.activePetTab === '猫咪' ? 'cat' : 'dog'
    const stage = GUIDE_DATA[key].find(s => s._id === stageid)
    if (!stage) return
    const item = stage.items.find(i => i._id === itemid)
    if (item) {
      this.setData({ showDetail: true, currentItem: item })
    }
  },

  closeDetail() {
    this.setData({ showDetail: false, currentItem: null })
  },

  noop() {},
})
